public class Wand {
  private String name;
  private double power;

  // ゲッターの設定
  public String getName() {
    return this.name;
  }
  public double getPower() {
    return this.power;
  }

  // セッターの設定
  public void setName(String name) {
    this.name = name;
  }
  public void setPower(double power) {
    this.power = power;
  }
}